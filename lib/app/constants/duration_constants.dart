/// A class that contains all the duration constants in the app
final class DurationConstants extends Duration {
  /// Duration of 250 milliseconds
  DurationConstants.ms250() : super(milliseconds: 250);

  /// Duration of 500 milliseconds
  DurationConstants.ms500() : super(milliseconds: 500);

  /// Duration of 1 second
  DurationConstants.s1() : super(seconds: 1);

  /// Duration of 2 seconds
  DurationConstants.s2() : super(seconds: 2);

  /// Duration of 3 seconds
  DurationConstants.s3() : super(seconds: 3);

  /// Duration of 4 seconds
  DurationConstants.s4() : super(seconds: 4);

  /// Duration of 5 seconds
  DurationConstants.s5() : super(seconds: 5);
}
